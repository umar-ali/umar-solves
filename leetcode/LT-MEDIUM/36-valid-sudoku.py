def solve(board: list[list[str]]) -> bool:
    ln = len(board)
    rows = []
    cols = []
    sqrs = []
    for i in range(ln):
        rows.append(set())
        cols.append(set())
        sqrs.append(set())

    for i in range(ln):
        for j in range(ln):
            sqridx = (i//3) * 3 + (j//3) # map (i, j) ot 0-8
            elem = board[i][j]
            if elem == ".": continue
            if elem in rows[i] or elem in cols[j]  or elem in sqrs[sqridx]:
                return False
            rows[i].add(elem)
            cols[j].add(elem)
            sqrs[sqridx].add(elem)
        
    return True


